  /// Récupère une devise par son code
  static Map<String, String>? getCurrencyByCode(String code) {
    try {
      return allCurrencies.firstWhere(
        (currency) => currency['code'] == code.toUpperCase(),
      );
    } catch (e) {
      return null;
    }
  }

  /// Récupère toutes les devises triées par code
  static List<Map<String, String>> getAllCurrenciesSorted() {
    final sorted = List<Map<String, String>>.from(allCurrencies);
    sorted.sort((a, b) => a['code']!.compareTo(b['code']!));
    return sorted;
  }

  /// Récupère toutes les devises triées par nom
  static List<Map<String, String>> getAllCurrenciesSortedByName() {
    final sorted = List<Map<String, String>>.from(allCurrencies);
    sorted.sort((a, b) => a['name']!.compareTo(b['name']!));
    return sorted;
  }

  /// Recherche des devises par nom ou code
  static List<Map<String, String>> searchCurrencies(String query) {
    final lowerQuery = query.toLowerCase();
    return allCurrencies.where((currency) {
      return currency['code']!.toLowerCase().contains(lowerQuery) ||
          currency['name']!.toLowerCase().contains(lowerQuery);
    }).toList();
  }
